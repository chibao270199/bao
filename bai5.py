class Hinh_Tron:
    def __init__(self,ban_kinh):
        self.r=ban_kinh
    def chu_vi(seft):
        return seft.r*2*3.14
    def dien_tich(seft):
        return seft.r*seft.r*3.14
r=int(input("Nhập Bán Kính Hình Tròn: "))
a = Hinh_Tron(r)
print("Chu Vi Hình Tròn là: ",a.chu_vi())
print("Diện Tích Hình Tròn là: ",a.dien_tich())